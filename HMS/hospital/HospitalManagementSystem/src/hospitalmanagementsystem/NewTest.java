    private void SumbitTestDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SumbitTestDetailsActionPerformed
        
       int x=0;
       while(x==0){  
        Test T=new Test();
        String s1=testid.getText();
        String s2=tn.getText();
        String s3=tpid.getText();
        String s4=ti.getText();
        String s5=ta.getText();
        if(s1.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Test ID");break;}else{T.setTestID(s1);}
        if(s2.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Test Name");break;}else{T.setTestName(s2);}
        if(s3.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient ID");break;}else{T.setRegID(s3);}
        if(s4.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Test Information");break;}else{T.setTestInformation(s4);}
        if(s5.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Test Amount");break;}else{T.setTestAmount(new Integer(s5).intValue());}
        T.setTestYear(new Integer(ty.getSelectedItem().toString()).intValue());
        T.setTestMonth(tm.getSelectedItem().toString());
        T.setTestDate(new Integer(td.getSelectedItem().toString()).intValue());
        if(dbo.addTest(T)) {
            JOptionPane.showMessageDialog(this, "Successfully Inserted");
            clearFields();
            break;
        } else {    
            JOptionPane.showMessageDialog(this, "Error while Inserting");
            clearFields();
            break;
        }
        
      }
      
    }//GEN-LAST:event_SumbitTestDetailsActionPerformed

