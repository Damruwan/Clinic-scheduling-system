    private void SubmitPatienDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitPatienDetailsActionPerformed
      int x=0;
      while(x==0){  
        Patient P=new Patient();
        String s1=pid.getText();
        String s2=tpfn.getText();
        String s3=tpad.getText();
        String s4=tpcn.getText();
        String s5=tpcdid.getText();
        String s6=tplcid.getText();
        String s7=tpwid.getText();
        String s8=tpag.getText();
        if(s1.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient ID");break;}else{P.setRegID(s1);}
        if(s2.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient First Name");break;}else{P.setFirstName(s2);}
        if(s3.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient Address");break;}else{P.setAddress(s3);}
        if(s4.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient Contact Number");break;}else{P.setContactNumber(new Integer(s4).intValue());}
        if(s5.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Conform Doctor ID");break;}else{P.setConformDoctorID(s5);}
        if(s6.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient Leading Consultant ID");break;}else{P.setLeadingConsultorID(s6);}
        if(s7.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient Ward ID");break;}else{P.setWardID(s7);}
        if(s8.isEmpty()){JOptionPane.showMessageDialog(this, "Please Enter Patient Age");break;}else{P.setAge(new Integer(s8).intValue()); }
        P.setLastName(tpln.getText());
        P.setGender(ddpg.getSelectedItem().toString());
        P.setDOBYear(new Integer(ddpdoby.getSelectedItem().toString()).intValue());
        P.setDOBMonth(ddpdobm.getSelectedItem().toString());
        P.setDOBDate(new Integer(ddpdobd.getSelectedItem().toString()).intValue());
        
        if(dbo.addPatient(P)) { 
            JOptionPane.showMessageDialog(this, "Successfully Inserted");
            clearFields();
            break;
        } else {    
            JOptionPane.showMessageDialog(this, "Error while Inserting");
            clearFields();
            break;
        }
      }
        
    }//GEN-LAST:event_SubmitPatienDetailsActionPerformed
    
